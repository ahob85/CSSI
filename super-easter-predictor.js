let readline = require("readline-sync");
console.log("******************************************************************");
console.log("Welcome to Super Easter Predictor!");
let y = readline.question("Please enter a year greater than 1582: ");
//console.log("y = " + y);
let a = y % 19;
//console.log("a = " + a);
let b = parseInt(y / 100);
//console.log("b = " + b);
let c = y % 100;
//console.log("c = " + c);
let d = b / 4;
//console.log("d = " + d);
let e = b % 4;
//console.log("e = " + e);
let f = parseInt((b + 8) / 25);
//console.log("f = " + f);
let g = parseInt((b - f + 1) / 3);
//console.log("g = " + g);
let h = (19 * a + b - d - g + 15) % 30;
//console.log("h = " + h);
let i = parseInt(c / 4);
//console.log("i = " + i);
let k = c % 4;
//console.log("k = " + k);
let r = (32 + 2 * e + 2 * i - h - k) % 7;
//console.log("r = " + r);
let m = parseInt((a + 11 * h + 22 * r) / 451);
//console.log("m = " + m);
let n = parseInt((h + r - 7 * m + 114) / 31);
//console.log("n = " + n);
let p = parseInt((h + r - 7 * m + 114) % 31);
//console.log("p = " + p);
let day = p + 1;
console.log("In " + y + ", Easter falls on " + n + "/" + day + ".");
